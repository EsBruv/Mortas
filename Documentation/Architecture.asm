    ; Zeropage: $0000 - $00FF
    ; RAM: $0100 - $07FF

    ; CHR 0: $0000 - $07FF (Sprites)
    ; CHR 1: $0800 - $0FFF (Sprites)

    ; CHR 2: $1000 - $17FF (Background)
    ; CHR 3: $1800 - $1FFF (Background)

    ; Save RAM: $6000 - $7FFF (Battery)

    ; Bank 0: $8000 - $9FFF (Switchable)
    ; Bank 1: $A000 - $BFFF (Switchable)
    ; Bank 2: $C000 - $DFFF (Fixed)
    ; Bank 3: $E000 - $FFFF (Fixed)



